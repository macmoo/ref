// ----------------------------
// 배열의 각요소에 대해 처리
// 베열.forEach(콜백함수)
// 인수로 건넨 콜백함수를 써서 요소를 순서대로 처리
// 콜백함수 (* : 생략가능)
// ([요소*], [인덱스*], [元배열*]) => {} 
const arr5 = ['딸기','귤','사과'];
arr5.forEach((value, index) => {
    console.log(index, value);
});
// 0 딸기
// 1 귤
// 2 사과
// -----------
[1,2,3,4,5,6,7]
.filter( (value) => value % 2 === 0)
.forEach((value) => {
    console.log(value);
}); // 2 4 6
