// 数値の変数同士の足し算
let number1 = 10;
let number2 = 20;
let sum = number1 + number2;
console.log(sum); // 結果：30

// 文字列の変数を結合する
let familyName = '鈴木';
let firstName = '太郎';
let fullName = familyName + firstName;
console.log(fullName); // 結果：'鈴木太郎'

// 変数を別の変数に代入する
let value1 = 100;
let value2 = value1;
console.log(value2); // 結果：100（value1と同じ値）
