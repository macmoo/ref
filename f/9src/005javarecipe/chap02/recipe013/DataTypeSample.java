package jp.co.shoeisha.javarecipe.chapter02.recipe013;

public class DataTypeSample {
	
	@SuppressWarnings("unused")
	public static void main(String[] args) {
		// 整数リテラル
		int i = 1;
		
		// 真偽リテラル
		boolean b = true;
		
		// 文字列リテラル
		String s = "文字列";
		
		// 数値リテラル（Java7以降）
		int a = 10_000_000; // => 10000000
		
	}
	
}
