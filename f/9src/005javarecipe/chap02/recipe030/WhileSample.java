package jp.co.shoeisha.javarecipe.chapter02.recipe030;

public class WhileSample {

	public static void main(String[] args) {

		/////////////////////////////////////////////////////////////////////////////
		// while文を使った繰り返し処理
		/////////////////////////////////////////////////////////////////////////////
		{
			int i = 0;

			while(i < 5) {
				i = (int)(Math.random() * 10);
				System.out.println(i);
			}
		}

		/////////////////////////////////////////////////////////////////////////////
		// do～while文を使った繰り返し処理
		/////////////////////////////////////////////////////////////////////////////
		{
			int i = 0;

			do {
				System.out.println("1回は必ず実行");

			} while(i > 0);
		}

	}

}
